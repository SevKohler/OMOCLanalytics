# SUMMARISE  -  the small statistics the sections need
# ================================================================================

def percentile(sorted_values, fraction):
    """The value at `fraction` (0.0 = minimum, 1.0 = maximum) of an already sorted list.

    The script targets Python 3.7, where statistics.quantiles does not exist yet, so the
    rule is spelled out: land on a position between the first and last item and interpolate
    linearly between the two neighbouring data points. This is the same "inclusive" rule
    numpy and later Python versions use, so the numbers are comparable.
    """
    if not sorted_values:
        return float("nan")
    position = fraction * (len(sorted_values) - 1)
    lower_index = int(position)
    upper_index = min(lower_index + 1, len(sorted_values) - 1)
    weight = position - lower_index
    return sorted_values[lower_index] * (1.0 - weight) + sorted_values[upper_index] * weight


def mean(values):
    return float(sum(values)) / len(values) if values else float("nan")


def median(values):
    return percentile(sorted(values), 0.5)


def percentage(part, whole):
    return 100.0 * part / whole if whole else float("nan")


