def main():
    archetype_leaf_counts = read_archetype_leaf_counts()      # archetype side
    omop_table_capacity = read_omop_table_capacity()          # OMOP side
    comparison_rows, unmatched_files, unknown_tables = build_comparison(archetype_leaf_counts,
                                                                        omop_table_capacity)

    # Join invariant: every mapping file must find its archetype. A file that does not join
    # would quietly shrink every number below, so this stops the run instead.
    if unmatched_files:
        raise ValueError(
            "%d mapping file(s) name an archetype that is not in archetypes.csv:\n  %s"
            % (len(unmatched_files),
               "\n  ".join("%s -> %r" % pair for pair in unmatched_files)))

    # Capacity invariant: every table the mappings target must have a prepared capacity, or
    # it drops out of the capacity calculation and inflates the result in the archetype's
    # favour. Only fact_relationship may legitimately be absent, and that is handled above.
    if unknown_tables:
        raise ValueError(
            "the mappings target %d OMOP table(s) with no row in omoptables.csv: %s. "
            "They would be silently ignored when computing capacity, which would overstate "
            "every result below."
            % (len(unknown_tables), ", ".join(sorted(unknown_tables))))

    print_aggregate(comparison_rows, omop_table_capacity)
    print_per_archetype(comparison_rows)
    print_per_omop_table(comparison_rows, omop_table_capacity)
    print_concentration(comparison_rows, omop_table_capacity)
    print_leaf_distribution(comparison_rows)
    print_arithmetic_floor(comparison_rows, read_measured_fragmentation())

    print()
    print("join check: %d mapping file(s) read, %d joined to an archetype, %d unmatched."
          % (len(comparison_rows), len(comparison_rows), len(unmatched_files)))


if __name__ == "__main__":
    main()
