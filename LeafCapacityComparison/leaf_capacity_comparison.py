"""
Compares the openEHR archetype leaf inventory against the capacity of the OMOP CDM tables
the archetypes map into, to support the manuscript's claim that archetypes carry far more
data nodes than their target OMOP tables can hold.

What it does, in plain terms:
  There are two sides to the comparison, kept clearly apart below.

  ARCHETYPE SIDE  what openEHR offers. Every archetype has a number of "leaves": its data
                  elements plus its cluster slots. That is how many distinct things the
                  archetype can record. Read from archetypes.csv.
  OMOP SIDE       what OMOP can accept. Every OMOP CDM table has been reduced to its
                  "content concepts": the columns that actually carry content, after
                  housekeeping columns (record ids, linkage fields, redundant
                  _source_value / _source_concept_id twins) are collapsed away. That is
                  how many distinct things the table can hold. Read from omoptables.csv.

  The two sides are joined through the OMOCL mapping files. Each mapping YAML names one
  archetype (`spec.openEhrConfig.archetype`) and lists the OMOP tables it writes into:

      mappings:
        - type: "Measurement"      <- this block writes a Measurement record
        - type: "Observation"      <- a second block, an Observation record

  An archetype's capacity is the widest table it maps into (the most content concepts any
  one of its target records can hold). Comparing leaves against that capacity shows how
  much of an archetype has no column to go to. This is deliberately a generous reading of
  OMOP: it credits the archetype with the best table it touches, and still the archetype
  is wider almost every time.

  Nothing here is estimated and nothing is hardcoded; every number printed is derived from
  the data sources below at run time.

Two conventions in the mapping files matter:
  - `type: Include` is an OMOCL directive (it plugs another archetype's mapping in), not an
    OMOP table, so it is excluded from the block counts entirely.
  - `type: CustomMapping` builds a fact_relationship row. It is a real mapping block and is
    counted as one, but Fact_relationship is pure linkage, has no prepared table, and so
    contributes no capacity.

Cross-check: section 4 counts the mapping blocks independently of
../Figure4Domains/domain_distribution.py, which counts the same blocks for Figure 4. The two
totals must agree. Run both; if they ever differ, one of them is wrong.

Data: ../resources/OMOCL mappings   (medical_data/** and person_data/**, tests excluded)
      ../resources/preparedArchetypeTables/preparedArchetypes/archetypes.csv
      ../resources/preparedOMOPTables/omoptables.csv
      ../T3Fragementation/resources/fragmentation.csv   (section 6 only; generated by
                                                         ../T3Fragementation/fragmentation.py,
                                                         which must be run first)
Requires: Python 3.7+ and PyYAML.   Run:  python "leaf_capacity_comparison.py"
"""
import os
import csv
import glob
import collections
import yaml

HERE = os.path.dirname(os.path.abspath(__file__))
RESOURCES = os.path.join(HERE, "..", "resources")

# OMOCL side: the mappings that join an archetype to its OMOP tables.
MAPPINGS_DIR = os.path.join(RESOURCES, "OMOCL mappings")
# Archetype side: how many leaves each archetype has.
ARCHETYPES_CSV = os.path.join(RESOURCES, "preparedArchetypeTables", "preparedArchetypes",
                              "archetypes.csv")
# OMOP side: how many content concepts each OMOP table can hold.
OMOP_TABLES_CSV = os.path.join(RESOURCES, "preparedOMOPTables", "omoptables.csv")

# Measured side, for section 6 only: what the mapping actually puts on the primary record.
# This is ../T3Fragementation's own output (Table 3), read here so the arithmetic floor and
# the measurement can be printed on one screen, over the same archetypes. Section 6 is
# skipped if it has not been generated.
FRAGMENTATION_CSV = os.path.join(HERE, "..", "T3Fragementation", "resources",
                                 "fragmentation.csv")

# "Include" plugs another archetype's mapping in at a base path. It reads like a block but
# names no OMOP table, so it is not a mapping block and is dropped before anything is counted.
NOT_A_TABLE = "Include"

# "CustomMapping" is how a fact_relationship row is built. It IS a mapping block (it is counted
# as one), but fact_relationship is linkage rather than content: it has no row in
# omoptables.csv, so an archetype that only reaches it has no capacity to compare against.
CUSTOM_MAPPING = "CustomMapping"
FACT_RELATIONSHIP = "Fact_relationship"

# A table this narrow can hold almost nothing; the concentration section counts how many
# mapping blocks aim at one.
NARROW_TABLE_CAPACITY = 5

# The leaf counts the distribution section reports "how many archetypes are wider than this".
LEAF_THRESHOLDS = (4, 5, 10, 20, 30)

# How many of the widest archetypes to list at the end.
WIDEST_ARCHETYPES_SHOWN = 10

# One archetype's row in the comparison: its leaves (archetype side) against the capacity of
# the tables its mapping targets (OMOP side). `capacity` is None when the mapping reaches no
# table that has a prepared capacity (fact_relationship only, or Include only).
ComparisonRow = collections.namedtuple(
    "ComparisonRow", "archetype elements slots leaves target_tables capacity")


# ================================================================================
# READ  -  the four data sources
# ================================================================================

def find_mapping_files():
    """Every OMOCL mapping YAML (medical_data + person_data), skipping the test fixtures."""
    paths = []
    for subfolder in ("medical_data", "person_data"):
        paths += glob.glob(os.path.join(MAPPINGS_DIR, subfolder, "**", "*.yml"), recursive=True)
    return sorted(path for path in paths if (os.sep + "tests" + os.sep) not in path)


def read_archetype_leaf_counts():
    """archetype id -> {elements, slots, leaves}, the archetype side of the comparison.

    From archetypes.csv, the vetted inventory: one row per archetype, `leaves` = the data
    elements plus the cluster slots it defines.
    """
    archetype_leaf_counts = collections.OrderedDict()
    with open(ARCHETYPES_CSV, encoding="utf-8") as handle:
        for row in csv.DictReader(handle):
            archetype_leaf_counts[row["archetype"]] = {
                "elements": int(row["elements"]),
                "slots": int(row["slots"]),
                "leaves": int(row["leaves"]),
            }
    return archetype_leaf_counts


def read_omop_table_capacity():
    """OMOP table -> content concepts it can hold, the OMOP side of the comparison.

    From omoptables.csv, where each CDM table has already been reduced to the columns that
    carry content (`removed` records how many were collapsed or dropped to get there).
    """
    omop_table_capacity = collections.OrderedDict()
    with open(OMOP_TABLES_CSV, encoding="utf-8") as handle:
        for row in csv.DictReader(handle):
            omop_table_capacity[row["table"]] = int(row["content_concepts"])
    return omop_table_capacity


def read_measured_fragmentation():
    """archetype id -> {leaves, mapped_primary}, or None if T3Fragementation has not run.

    The measured side of section 6: fragmentation.py reads the same prepared leaves, matches
    each one to the OMOCL mapping by its at-code, and records how many actually landed on the
    archetype's primary OMOP record. Everything else needs a separate record.
    """
    if not os.path.exists(FRAGMENTATION_CSV):
        return None
    measured = {}
    with open(FRAGMENTATION_CSV, encoding="utf-8") as handle:
        for row in csv.DictReader(handle):
            measured[row["archetype"]] = {
                "leaves": int(row["leaves"]),
                "mapped_primary": int(row["mapped_primary"]),
            }
    return measured


def read_mapping_targets(mapping_file):
    """One mapping YAML -> (archetype id it maps, [OMOP table per mapping block]).

    The table list keeps duplicates on purpose: an archetype that writes twelve Measurement
    records has twelve Measurement blocks, and the block count is what Figure 4 reports.
    """
    content = yaml.safe_load(open(mapping_file, encoding="utf-8")) or {}
    spec = content.get("spec") or {}
    archetype_id = (spec.get("openEhrConfig") or {}).get("archetype")

    target_tables = []
    for block in content.get("mappings", []) or []:
        if not isinstance(block, dict) or "type" not in block:
            continue
        block_type = block["type"]
        if block_type == NOT_A_TABLE:
            continue                                  # a directive, not a table
        target_tables.append(FACT_RELATIONSHIP if block_type == CUSTOM_MAPPING else block_type)
    return archetype_id, target_tables


# ================================================================================
# JOIN  -  every mapping file to its archetype, and to its tables' capacity
# ================================================================================

def build_comparison(archetype_leaf_counts, omop_table_capacity):
    """One ComparisonRow per mapping file, plus the list of files that failed to join.

    DATA QUIRK, not a general rule: two mapping files spell the archetype id with different
    casing than the archetype inventory does
    (`openEHR-EHR-CLUSTER.Oocyte_specimen.v1`, `openEHR-EHR-Evaluation.pregnancy_status.v0`).
    openEHR archetype ids are otherwise case-consistent, so the case-insensitive lookup here
    exists only to absorb those two typos and let all 198 files join. Any file that still
    fails to join is returned and reported loudly by the caller, never dropped in silence.
    """
    canonical_id = {archetype.lower(): archetype for archetype in archetype_leaf_counts}

    comparison_rows = []
    unmatched_files = []
    unknown_tables = set()
    for mapping_file in find_mapping_files():
        mapping_archetype_id, target_tables = read_mapping_targets(mapping_file)
        archetype = canonical_id.get((mapping_archetype_id or "").lower())
        if archetype is None:
            unmatched_files.append((os.path.basename(mapping_file), mapping_archetype_id))
            continue

        # Fact_relationship is the ONLY table allowed to have no prepared capacity: it is
        # linkage and holds no content. Any other unknown table would be silently left out
        # of the max below, which would raise every capacity-based number in the archetype's
        # favour. Collected here and raised by the caller rather than quietly skipped.
        unknown_tables.update(set(target_tables) - set(omop_table_capacity)
                              - {FACT_RELATIONSHIP})

        # The archetype's capacity is the widest table it writes into: the most content
        # concepts any single one of its OMOP records can hold. Tables with no prepared
        # capacity (fact_relationship) cannot contribute one, so an archetype that reaches
        # nothing else has no capacity at all and is excluded from capacity comparisons.
        capacities = [omop_table_capacity[table] for table in target_tables
                      if table in omop_table_capacity]

        counts = archetype_leaf_counts[archetype]
        comparison_rows.append(ComparisonRow(
            archetype=archetype,
            elements=counts["elements"],
            slots=counts["slots"],
            leaves=counts["leaves"],
            target_tables=target_tables,
            capacity=max(capacities) if capacities else None,
        ))
    comparison_rows.sort(key=lambda row: row.archetype)
    return comparison_rows, unmatched_files, unknown_tables


