def print_aggregate(comparison_rows, omop_table_capacity):
    """Section 1: the size of each side. Inventory only, deliberately not a ratio.

    The two totals are NOT divided into each other. The archetype total counts nodes across
    198 separate artifacts; the OMOP total counts columns across 10 table definitions. A
    ratio between them would measure "198 > 10" rather than any loss of information, since
    OMOP is free to write unlimited rows into a table. The comparison that carries meaning
    is per archetype, against the table its own mapping reaches: section 2.
    """
    total_elements = sum(row.elements for row in comparison_rows)
    total_slots = sum(row.slots for row in comparison_rows)
    total_leaves = sum(row.leaves for row in comparison_rows)
    total_capacity = sum(omop_table_capacity.values())

    print("=" * 78)
    print("1. INVENTORY  -  the size of each side (context for section 2, not a claim)")
    print("=" * 78)
    print("archetypes mapped                 : %d" % len(comparison_rows))
    print("  data elements                   : %d" % total_elements)
    print("  cluster slots                   : %d" % total_slots)
    print("  leaves (elements + slots)       : %d" % total_leaves)
    print("OMOP tables prepared              : %d" % len(omop_table_capacity))
    print("  content concepts (total)        : %d" % total_capacity)


def print_per_archetype(comparison_rows):
    """Section 2: each archetype against the widest table its own mapping reaches."""
    with_capacity = [row for row in comparison_rows if row.capacity is not None]
    without_capacity = [row for row in comparison_rows if row.capacity is None]

    over_capacity = [row for row in with_capacity if row.leaves > row.capacity]
    leaves = [row.leaves for row in with_capacity]
    capacities = [row.capacity for row in with_capacity]

    print()
    print("=" * 78)
    print("2. PER ARCHETYPE  -  leaves against the capacity of its own target table")
    print("=" * 78)
    print("(capacity = the most content concepts any single table the archetype maps into")
    print(" can hold, i.e. the most generous table available to it)")
    print()
    print("archetypes with a target capacity : %d" % len(with_capacity))
    print("  more leaves than capacity       : %d  (%.1f%%)"
          % (len(over_capacity), percentage(len(over_capacity), len(with_capacity))))
    print("  leaves     median / mean        : %.1f / %.1f" % (median(leaves), mean(leaves)))
    print("  capacity   median / mean        : %.1f / %.1f"
          % (median(capacities), mean(capacities)))
    print("  summed leaves vs summed capacity: %d vs %d  (%.1f x)"
          % (sum(leaves), sum(capacities), float(sum(leaves)) / sum(capacities)))

    # Not a silent drop: these archetypes reach no table with a prepared capacity, so there
    # is nothing to compare them against. They are named so the gap is visible, not hidden.
    if without_capacity:
        print()
        print("archetypes with no target capacity: %d  (excluded from the comparison above;"
              % len(without_capacity))
        print("  their mapping reaches no prepared table)")
        for row in without_capacity:
            targets = ", ".join(sorted(set(row.target_tables))) or "no table (Include only)"
            print("    %-58s %d leaves -> %s" % (row.archetype, row.leaves, targets))


def print_per_omop_table(comparison_rows, omop_table_capacity):
    """Section 3: the load each OMOP table is asked to carry."""
    blocks_per_table = collections.Counter()
    archetypes_per_table = collections.defaultdict(set)

    for row in comparison_rows:
        for table in row.target_tables:
            blocks_per_table[table] += 1
        for table in set(row.target_tables):
            archetypes_per_table[table].add(row.archetype)

    print()
    print("=" * 78)
    print("3. PER OMOP TABLE  -  where the mapping load lands")
    print("=" * 78)
    print("%-22s %9s %8s %11s" % ("Table", "Capacity", "Blocks", "Archetypes"))
    print("-" * 53)
    for table, block_count in sorted(blocks_per_table.items(),
                                     key=lambda pair: pair[1], reverse=True):
        capacity = omop_table_capacity.get(table)
        print("%-22s %9s %8d %11d"
              % (table,
                 capacity if capacity is not None else "-",     # fact_relationship holds no content
                 block_count,
                 len(archetypes_per_table[table])))
    print("-" * 53)
    print("%-22s %9d %8d %11d" % ("total", sum(omop_table_capacity.values()),
                                  sum(blocks_per_table.values()),
                                  len(comparison_rows)))
    print()
    multi_table = sum(1 for row in comparison_rows if len(set(row.target_tables)) > 1)
    print("Deliberately no leaf column here. Totalling the leaves of every archetype that")
    print("targets a table would count the %d multi-table archetypes once per table, and"
          % multi_table)
    print("comparing that total to a single row's capacity would compare an inventory to a")
    print("row width. Leaves are compared per archetype in section 2, where they belong.")


def print_concentration(comparison_rows, omop_table_capacity):
    """Section 4: how much of the mapping is funnelled into the narrowest tables."""
    total_blocks = sum(len(row.target_tables) for row in comparison_rows)
    # Membership is tested FIRST: fact_relationship has no prepared capacity, and a table
    # with no known width is not evidence of a narrow one. It counts in total_blocks (it is
    # a real mapping block) but can never count as narrow.
    narrow_blocks = sum(1
                        for row in comparison_rows
                        for table in row.target_tables
                        if table in omop_table_capacity
                        and omop_table_capacity[table] <= NARROW_TABLE_CAPACITY)

    print()
    print("=" * 78)
    print("4. CONCENTRATION  -  how many mapping blocks land in a very narrow table")
    print("=" * 78)
    print("mapping blocks (total)            : %d" % total_blocks)
    print("  targeting a table with <= %d     : %d  (%.1f%%)"
          % (NARROW_TABLE_CAPACITY, narrow_blocks, percentage(narrow_blocks, total_blocks)))
    print("  content concepts")
    print()
    print("Cross-check: this block total is computed independently of, and must agree with,")
    print("the total ../Figure4Domains/domain_distribution.py prints for Figure 4. Run both")
    print("and compare; if they differ, one of them is wrong.")


def print_leaf_distribution(comparison_rows):
    """Section 5: the shape of the archetype side, and the extreme cases."""
    sorted_leaves = sorted(row.leaves for row in comparison_rows)

    print()
    print("=" * 78)
    print("5. DISTRIBUTION  -  leaves per archetype")
    print("=" * 78)
    print("min / p25 / median / p75 / max     : %d / %.1f / %.1f / %.1f / %d"
          % (sorted_leaves[0], percentile(sorted_leaves, 0.25), percentile(sorted_leaves, 0.50),
             percentile(sorted_leaves, 0.75), sorted_leaves[-1]))
    print()
    print("archetypes with more leaves than:")
    for threshold in LEAF_THRESHOLDS:
        above = sum(1 for leaves in sorted_leaves if leaves > threshold)
        print("  %2d leaves                       : %3d  (%.1f%%)"
              % (threshold, above, percentage(above, len(sorted_leaves))))

    widest = sorted(comparison_rows, key=lambda row: row.leaves,
                    reverse=True)[:WIDEST_ARCHETYPES_SHOWN]
    print()
    print("the %d widest archetypes:" % len(widest))
    print("%-52s %7s %9s %7s" % ("Archetype", "Leaves", "Capacity", "Ratio"))
    print("-" * 78)
    for row in widest:
        if row.capacity is not None:
            capacity_text = "%d" % row.capacity
            ratio_text = "%.1fx" % (float(row.leaves) / row.capacity)
        else:
            capacity_text, ratio_text = "-", "-"      # no prepared table to compare against
        print("%-52s %7d %9s %7s" % (row.archetype, row.leaves, capacity_text, ratio_text))


def print_arithmetic_floor(comparison_rows, measured_fragmentation):
    """Section 6: how much of the measured fragmentation is forced by counting alone.

    Why this section exists. The obvious objection to the measured fragmentation is that it
    is an artefact of how we chose to map, i.e. that a better mapping would keep more nodes
    on the primary record. This section answers that without using the mapping at all.

    Count an archetype's nodes, count the content fields of the table it targets, and assume
    -- generously, and counterfactually -- that ANY node could occupy ANY field. Whatever is
    left over has no field to go to under any mapping whatsoever. That is a floor: no mapping
    decision can push fragmentation below it.

    Both numbers answer the SAME question (what fits on one primary record?) over the SAME
    archetypes, which is what makes them comparable: the floor answers it by counting fields,
    fragmentation.py answers it by reading the mapping. The floor must come out at or below
    the measurement.

    What the gap between them is NOT. This script does not measure why the gap exists and no
    claim about its cause can be read out of these numbers. Two candidates are indistinguish-
    able here: OMOP's fields are typed, so a Severity node cannot occupy condition_start_date
    even when that column is empty; and a mapping may leave a usable field unused. Only the
    floor itself is established, and only as a lower bound.

    This is a lower bound, NOT an estimate of fragmentation. It sits well below the measured
    value by construction, and reporting it as a prediction would misrepresent it. What it
    supports is one bounded claim: at least this share of the fragmentation is forced by the
    size difference alone and holds under any mapping whatsoever.
    """
    with_capacity = [row for row in comparison_rows if row.capacity is not None]

    total_nodes = sum(row.leaves for row in with_capacity)
    total_fields = sum(row.capacity for row in with_capacity)
    # Per archetype, because capacity is per archetype: a field can only hold a node of the
    # archetype whose record it belongs to. Summing first would let one archetype's spare
    # columns absorb another archetype's overflow, which no mapping could ever do.
    coverable = sum(min(row.leaves, row.capacity) for row in with_capacity)
    excess = sum(max(0, row.leaves - row.capacity) for row in with_capacity)

    print()
    print("=" * 78)
    print("6. ARITHMETIC FLOOR  -  fragmentation forced by size alone, before any mapping")
    print("=" * 78)
    print("(assumes any node could occupy any field, which no real mapping achieves;")
    print(" whatever still does not fit cannot fit under ANY mapping)")
    print()
    print("archetypes compared               : %d" % len(with_capacity))
    print("  archetype nodes                 : %d" % total_nodes)
    print("  content fields available        : %d" % total_fields)
    print("  nodes a field could cover       : %d  (%.1f%%)"
          % (coverable, percentage(coverable, total_nodes)))
    print("  nodes MORE than OMOP can cover  : %d  (%.1f%%)   <- the floor"
          % (excess, percentage(excess, total_nodes)))

    if measured_fragmentation is None:
        print()
        print("Measured comparison skipped: ../T3Fragementation/resources/fragmentation.csv")
        print("has not been generated. Run ../T3Fragementation/fragmentation.py first.")
        return

    # SAME ARCHETYPES ON BOTH SIDES. The whole section rests on the two numbers sharing a
    # denominator: the corpus-wide Table 3 figure is over all 198 archetypes, and comparing
    # that to a floor over 196 would be exactly the units error this comparison exists to
    # avoid. So the floor is recomputed over the archetypes the measurement also covers,
    # rather than trusting that the two sets happen to coincide.
    comparable = [row for row in with_capacity if row.archetype in measured_fragmentation]

    # No silent subsetting. If fragmentation.csv does not cover every archetype compared
    # above, the section would quietly compare a subset and still print a conclusion that
    # reads as though it covered them all -- internally consistent, and wrong.
    uncovered = [row.archetype for row in with_capacity if row.archetype not in measured_fragmentation]
    if uncovered:
        raise ValueError(
            "fragmentation.csv is missing %d of the %d archetypes compared above (e.g. %s). "
            "It is stale or incomplete: re-run ../T3Fragementation/fragmentation.py. "
            "Comparing only the archetypes it happens to cover would silently narrow the "
            "claim while still printing it as though it held for all of them."
            % (len(uncovered), len(with_capacity), ", ".join(sorted(uncovered)[:3])))

    floor_nodes = sum(row.leaves for row in comparable)
    floor_excess = sum(max(0, row.leaves - row.capacity) for row in comparable)
    measured_nodes = sum(measured_fragmentation[row.archetype]["leaves"] for row in comparable)
    measured_primary = sum(measured_fragmentation[row.archetype]["mapped_primary"]
                           for row in comparable)
    measured_auxiliary = measured_nodes - measured_primary

    floor_percentage = percentage(floor_excess, floor_nodes)
    measured_percentage = percentage(measured_auxiliary, measured_nodes)

    # Both invariants are checked BEFORE anything is printed. A failing invariant means the
    # numbers are not fit to publish, and printing the conclusion prose first would put a
    # false claim into a reviewer's log even though the run later aborts.
    if floor_nodes != measured_nodes:
        raise ValueError(
            "the two sides disagree about the node count over the same %d archetypes "
            "(floor %d, measured %d): archetypes.csv and fragmentation.csv have drifted "
            "apart, so neither number should be published."
            % (len(comparable), floor_nodes, measured_nodes))
    if floor_percentage > measured_percentage:
        raise ValueError(
            "arithmetic floor (%.1f%%) exceeds measured fragmentation (%.1f%%): the two "
            "sides disagree about what fits on a primary record, so one of them is wrong."
            % (floor_percentage, measured_percentage))

    print()
    print("measured over the same %d archetypes (from T3Fragementation):" % len(comparable))
    print("  archetype nodes                 : %d" % measured_nodes)
    print("  mapped to the primary record    : %d  (%.1f%%)"
          % (measured_primary, percentage(measured_primary, measured_nodes)))
    print("  needing a separate record       : %d  (%.1f%%)"
          % (measured_auxiliary, measured_percentage))
    print()
    print("floor %.1f%%  <=  measured %.1f%%   (gap %.1f points)"
          % (floor_percentage, measured_percentage, measured_percentage - floor_percentage))
    print()
    print("The floor lies below the measurement, as it must. At least %.1f of the %.1f measured"
          % (floor_percentage, measured_percentage))
    print("points follow from the size difference between the two models alone: those nodes have")
    print("no field on the primary record under ANY mapping, so that share cannot be attributed")
    print("to mapping decisions. The remaining %.1f points are NOT attributed by this script -"
          % (measured_percentage - floor_percentage))
    print("distinguishing typed fields from fields left unused needs the per-field analysis in")
    print("../T3Fragementation, not this counting argument.")


